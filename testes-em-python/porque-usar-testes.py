# Boas praticas de programação

# 1 ) -->  Deixar o codigo facil de entender, aplicar a identação e organizar bem o codigo!
# nomenclatura de funções, classes e variaveis . --> boa identação 


# 2 ) --> sempre refaturar o codigo a fim de diminuir o tamanho do codigo, porem sem perder sua funcionalidade e organização (facil entendimento)


# 3 ) --> Atingir o objetivo do codigo sem dar muitas voltas ( com o tempo e experiencia temos a melhora desse âmbito )


# 4 ) --> Testar seu codigo tbm é uma boa pratica

# portando vamos introduzir

# 4.1 ) testes são uma maneira de automatizar todos os testes de todas as partes do seu programa ( podemos fazer isso pra classes, variaveis e etc.. literalmente tudo )

# 4.2 ) porque usa-los --> Reduz bugs, mesmo adicionando mesmo adicionando novos codigos posteriormente
# seu codigo adquiri qualidade
# cria maior acessibilidade ao seu codigo para ser usado para outros programadores

# 4.3 ) como usa-los

# Assertions, Doctestes, Unittest , dentro outros

# OBS --> o mais indicado é o Unittest!


# 4.4 ) quando aplica-los --> Sempre que possivel 

# 4.5 ) onde aplica-los --> Geralmente são mais usados em classes e funções, mas podem ser usados no codigo todo
# são aplicados em Desenvolvimento Web, aplicativos, jogos, dentre outros

# 4.6)  lembrando que aqui nós vamos testar o codigo/ que é diferente de tratar erros no codigo!
# porem ambos andam lado a lado

# 4.7 ) TDD --> teste Driven Development --> Desenvolvimento Orientado a Testes
# basicamente é um passo a passo de como desenvolver um programa com testes
# se divide em 3 etapas

# a )  Red: --> aplica o teste no codigo sem ser implementado, já esperando a falha!

# b ) Green: --> desenvolve o codigo para passar no teste

# c ) Refactor: --> Voce refatora o codigo para ficar mais funcional


